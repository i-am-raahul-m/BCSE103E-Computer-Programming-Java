public class Var
{
	public static void main(String[] args)
	{
        // Defining variables of different datatypes, definition = declaration + initialization
		int int_var = 34;
		float float_var = 67.56f;
		double double_var = 54.84786;
		boolean bool_var = true;
		char char_var = 'g';
		String str_var = "Java lab";
        // Changing variable value
        int_var = 67;
        // Printing to screen
		System.out.println(int_var);
		System.out.println(float_var);
		System.out.println(double_var);
		System.out.println(bool_var);
		System.out.println(char_var);
		System.out.println(str_var);
	}
}

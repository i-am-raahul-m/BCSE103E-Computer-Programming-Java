public class MultipleAttributes {
    int age = 24;
    String fname = "John";
    String lname = "Doe";
    public static void main(String[] args)
    {
        MultipleAttributes myObj = new MultipleAttributes();
        System.out.println("Nme: " + myObj.fname + " " + myObj.lname);
        System.out.println("Age: " + myObj.age);
    }
}
